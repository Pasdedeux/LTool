    public const long TIME_HOUR_SC = 60 * TIME_MINUTE_SC;
    public const long TIME_DAY_SC = 24 * TIME_HOUR_SC;
    public const long TIME_WEEK_SC = 7 * TIME_DAY_SC;
    public const long TIME_YEAR_SC = 365 * TIME_DAY_SC;


}
